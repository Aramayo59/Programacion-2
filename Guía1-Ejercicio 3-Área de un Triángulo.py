# Escribe un programa en Python que solicite al usuario que ingrese la base y la altura de un triángulo, y luego calcule y muestre el área del triángulo.

base = float(input("Ingresa la base del triángulo: "))
altura = float(input("Ingresa la altura del triángulo: "))


area = (base * altura) / 2


print("El área del triángulo es:", area)
